#include <stdio.h>

int main(){

    int a=3,b=5;

    printf("%d\n",a==b);    //compares variables

    printf("%d\n",a>b);     //checks if a is greater than b

    printf("%d\n",a>=b);    //checks if a is greater than or equal to b                  

    printf("%d\n",a<b);     //checks if a is lesser than b

    printf("%d\n",a<=b);    //checks if a is lesser than or equal to b

    printf("%d\n",a!=b);    //checks if a is not equal to b

    return 0;
}